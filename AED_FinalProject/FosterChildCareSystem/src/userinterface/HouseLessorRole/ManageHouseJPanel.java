    public void PopulateHouses(){
        DefaultTableModel model = (DefaultTableModel) tblHouseList.getModel();

        model.setRowCount(0);
        for(House h: this.system.getHouseDirectory().getHouseList()){
            if(h.getHouseLessor().Name.equals(this.currentHouseLessor.getName()));
            Object[] row = new Object[4];
                row[0] = h.getId();
                row[1] = h.getHouseName();
                row[2] = h.getAddress();
                row[3] = h.getCity();
                model.addRow(row);
        }
    }

