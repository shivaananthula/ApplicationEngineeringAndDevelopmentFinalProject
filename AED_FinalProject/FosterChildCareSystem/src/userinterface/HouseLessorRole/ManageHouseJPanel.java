    private void AddHouseActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_AddHouseActionPerformed
        // TODO add your handling code here:
        
        
        if(txtName.getText()!= null)
        {
            String Name=null;
        String Address=null;
        String City=null;
        if(txtName.getText() != "")
            Name= txtName.getText();
        
        if(txtAddress.getText()!= ""){
            Address = txtAddress.getText();
        }
        
        if(txtCity1.getText()!= null)
            City = txtCity1.getText();
        
        House newHouse = system.getHouseDirectory().CreateHouse(Name, City, Address);
        newHouse.setHouseLessor(currentHouseLessor);
        JOptionPane.showMessageDialog(null, "House Added Successfully");
        PopulateHouses();
        txtName.setText("");
        txtAddress.setText("");
        txtCity1.setText("");
        }
        else{
             JOptionPane.showMessageDialog(null, "Please Enter Value", "Warning", JOptionPane.WARNING_MESSAGE);
        }
        
        
    }//GEN-LAST:event_AddHouseActionPerformed

    private void DeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DeleteActionPerformed
        // TODO add your handling code here:
        int selectedRow = tblHouseList.getSelectedRow();
        
        if(selectedRow >= 0){
            DefaultTableModel tableRecords = (DefaultTableModel)tblHouseList.getModel();
            int id = (int)tableRecords.getValueAt(selectedRow, 0);
            House h = system.houseDirectory.getHouseById(id);
            system.houseDirectory.HouseList.remove(h);
            JOptionPane.showMessageDialog(null, "House deleted Successfully");
            PopulateHouses();
        }
    }//GEN-LAST:event_DeleteActionPerformed


