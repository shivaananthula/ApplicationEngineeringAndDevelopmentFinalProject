     private void populateTable() {
        
        DefaultTableModel model = (DefaultTableModel) tblCounselors.getModel();
        model.setRowCount(0);
        
        for(Enterprise enterprise: network.getEnterpriseDirectory().getEnterpriseList()){
            
            if(enterprise.getEnterpriseType().toString().equals("Training Center")){
                for(ChildCounsellor ins: enterprise.getChildCounsellorDirectory().ChildCounsellorList){
                    Object[] row = new Object[model.getColumnCount()];
                    row[0]= ins;
                row[1] = ins.getChildCounsellorId();
                row[2] = ins.getName();
                row[3] = ins.getPhoneNumber();
                row[4] = ins.getAddress();
               ((DefaultTableModel) tblCounselors.getModel()).addRow(row);
                }
            } 
    }
    }

