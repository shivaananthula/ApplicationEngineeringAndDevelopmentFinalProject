    private void btnEnrollActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEnrollActionPerformed
        // TODO add your handling code here:

        int selectedRow = tblCounselors.getSelectedRow();

        if(selectedRow >=0){
            DefaultTableModel tableRecords = (DefaultTableModel)tblCounselors.getModel();
            ChildCounsellor couns = (ChildCounsellor)tableRecords.getValueAt(selectedRow, 0);

            EnrollforCounselingWorkRequest enrolReq = new EnrollforCounselingWorkRequest();
            enrolReq.setEnterprise(Enterprise);
            enrolReq.setNetwork(network);
            enrolReq.setFosterChild(CurrentChild);
            enrolReq.setChildCounselor(couns);
            enrolReq.setStatus("Training Enroll requested.");
            enrolReq.setReqId(system.getWorkQueue().getWorkRequestList().size()+1);

            system.getWorkQueue().getWorkRequestList().add(enrolReq);

            JOptionPane.showMessageDialog(null, " Training Enrolled Requested.");

        }
    }//GEN-LAST:event_btnEnrollActionPerformed

